    private void btmDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmDeletarActionPerformed
        // TODO add your handling code here:
         Cliente cliente = new Cliente();
         
         cliente.setTelefonePesquisar(jTextFieldTelefonePesquisar.getText());
         
         if ((jTextFieldTelefonePesquisar.getText().isEmpty()) ) {
          JOptionPane.showMessageDialog(null, "Insira um cliente a ser deletado");
          }else{
             try {
                 ClienteDAO dao = new ClienteDAO();
                 dao.deletar(cliente);
                 JOptionPane.showMessageDialog(null, "Cliente do telefone "+jTextFieldTelefonePesquisar.getText()+" deletado com sucesso! ");
                 jTextFieldTelefonePesquisar.setText("");
                 jTextFieldNome.setText("");
                jTextFieldTelefone.setText("");
                jTextFieldRua.setText("");
                jTextFieldBairro.setText("");
                jTextFieldNumeroDaCasa.setText("");
                jTextFieldPedido.setText("");
                jTextFieldDescricao.setText("");
                jTextFieldQuantidade.setText("");
                jTextFieldPreco.setText("");
             } catch (SQLException ex) {
                 Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
             }
          }
    }//GEN-LAST:event_btmDeletarActionPerformed


    private void btmAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmAtualizarActionPerformed
        // TODO add your handling code here:
        Cliente clientes = new Cliente();
        
    clientes.setTelefonePesquisar(jTextFieldTelefonePesquisar.getText());
    clientes.setNome(jTextFieldNome.getText());
    clientes.setTelefone(jTextFieldTelefone.getText());
    clientes.setRua(jTextFieldRua.getText());
    clientes.setBairro(jTextFieldBairro.getText());
    clientes.setNumeroDaCasa(jTextFieldNumeroDaCasa.getText());
    clientes.setPedido(jTextFieldPedido.getText());
    clientes.setDescricao(jTextFieldDescricao.getText());
    clientes.setQuantidade(jTextFieldQuantidade.getText());
    clientes.setPreco(jTextFieldPreco.getText());

    if (jTextFieldNome.getText().isEmpty() || jTextFieldTelefone.getText().isEmpty() || jTextFieldRua.getText().isEmpty() || jTextFieldBairro.getText().isEmpty()
        || jTextFieldNumeroDaCasa.getText().isEmpty() || jTextFieldPedido.getText().isEmpty() || jTextFieldQuantidade.getText().isEmpty() || jTextFieldDescricao.getText().isEmpty() || jTextFieldPreco.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.");
    } else {
            try {
                ClienteDAO dao = new ClienteDAO();
                dao.atualizar(clientes);
                JOptionPane.showMessageDialog(null, "Pedido atualizado com sucesso");
                jTextFieldNome.setText("");
                jTextFieldTelefone.setText("");
                jTextFieldRua.setText("");
                jTextFieldBairro.setText("");
                jTextFieldNumeroDaCasa.setText("");
                jTextFieldPedido.setText("");
                jTextFieldDescricao.setText("");
                jTextFieldQuantidade.setText("");
                jTextFieldPreco.setText("");
                jTextFieldTelefonePesquisar.setText("");
                dao.desconectar();
            } catch (SQLException ex) {
                Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
        
    }//GEN-LAST:event_btmAtualizarActionPerformed

    private void btmFinalizarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmFinalizarPedidoActionPerformed
        // TODO add your handling code here:
        Cliente clientes = new Cliente();
        
        clientes.setNome(jTextFieldNome.getText());
        clientes.setTelefone(jTextFieldTelefone.getText());
        clientes.setRua(jTextFieldRua.getText());
        clientes.setBairro(jTextFieldBairro.getText());
        clientes.setNumeroDaCasa(jTextFieldNumeroDaCasa.getText());
        clientes.setPedido(jTextFieldPedido.getText());
        clientes.setDescricao(jTextFieldDescricao.getText());
        clientes.setQuantidade(jTextFieldQuantidade.getText());
        clientes.setPreco(jTextFieldPreco.getText());

        if (jTextFieldNome.getText().isEmpty() || jTextFieldTelefone.getText().isEmpty() || jTextFieldRua.getText().isEmpty() || jTextFieldBairro.getText().isEmpty()
            || jTextFieldNumeroDaCasa.getText().isEmpty() || jTextFieldPedido.getText().isEmpty() || jTextFieldQuantidade.getText().isEmpty() || jTextFieldDescricao.getText().isEmpty() || jTextFieldPreco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
        } else {
            ClienteDAO dao = new ClienteDAO();
            try {
                dao.finalizarPedido(clientes);
                JOptionPane.showMessageDialog(null, "Pedido finalizado com sucesso");
                jTextFieldNome.setText("");
                jTextFieldTelefone.setText("");
                jTextFieldRua.setText("");
                jTextFieldBairro.setText("");
                jTextFieldNumeroDaCasa.setText("");
                jTextFieldPedido.setText("");
                jTextFieldDescricao.setText("");
                jTextFieldQuantidade.setText("");
                jTextFieldPreco.setText("");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao finalizar o pedido");
                ex.printStackTrace();
            } finally {
                dao.desconectar();
            }
        }
    

        
    }//GEN-LAST:event_btmFinalizarPedidoActionPerformed

    private void btmPesquisarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmPesquisarClienteActionPerformed
        // TODO add your handling code here:
        String telefone;
        telefone = jTextFieldTelefonePesquisar.getText();
        ClienteDAO dao = new ClienteDAO();

               try {
                   Cliente cliente = dao.pesquisar(telefone);
                   if (cliente == null){
                       JOptionPane.showMessageDialog(null,"Cliente não localizado");
                   }else{
                       jTextFieldNome.setText(cliente.getNome());
                       jTextFieldTelefone.setText(cliente.getTelefone());
                       jTextFieldRua.setText(cliente.getRua());
                       jTextFieldBairro.setText(cliente.getBairro());
                       jTextFieldNumeroDaCasa.setText(cliente.getNumeroDaCasa());
                       jTextFieldPedido.setText(cliente.getPedido());
                       jTextFieldDescricao.setText(cliente.getDescricao());
                       jTextFieldQuantidade.setText(cliente.getQuantidade());
                       jTextFieldPreco.setText(cliente.getPreco());                      
                   }
                   dao.desconectar();
               } catch (SQLException ex) {
                       JOptionPane.showMessageDialog(null,"SQLException");
               }
    }//GEN-LAST:event_btmPesquisarClienteActionPerformed

